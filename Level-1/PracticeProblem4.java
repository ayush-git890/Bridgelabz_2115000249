public class PracticeProblem4 {
    public static void main(String[] args) {
        //cost price
        int cp = 129;
        //selling price
        int sp = 191;
        //the profit will be calculated as
        int profit = sp-cp;
        //the profit percentage will be calculated as
        double pp = (profit/cp) * 100;
        System.out.println("The Cost Price is INR "+129+" and Selling Price is INR "+sp+" The Profit is INR "+profit+" and the Profit Percentage is "+pp);
    }
}
